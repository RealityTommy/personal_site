			<h2>LEAPTweets</h2>
			
			<p>Project LEAPTweets is the result of a collaborative effort between Michigan State University students within the College of Arts and Letters and the College of Engineering. The aim of this project is to bridge a closer, more personal connection between the participants attending the HASTAC 2015 conference and the online Twitter community through a visual and interactive physical interface.</p>
			<p>This project features a program that incorporates Twitter APIs, the programming languages of Processing and Java, and the use of the LEAP Motion input device. In order to provide participants with a more unique relationship with Twitter, LEAPTweets first acts as a live Tweet wall, displaying over a hundred Tweets at a time streaming across five displays, queried by event-specific hashtags and topics. Then, to engage people more interactively, the LEAP Motion allows them to seamlessly move their hand across the displays, highlighting a Tweet of their choice to display larger and become easier to read and share.</p>
			
