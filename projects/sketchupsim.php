			<h2>SketchUpSim</h2>
			
			<p>The issue SketchUpSim aims to address is the discrepancy between the content educators want to make and the time or knowledge they have to create it. With the increase popularity of virtual reality in education, many educators want to take advantage of this technology to make content that both engages and excites students with the lesson on a level deeper than some traditional methods. Unfortunately, due to constraints imposed by budgets, time, experience, etc., not every educator can make use of these opportunities to connect with their students.</p>
			<p>In response, the goal of the SketchUpSim project is to create a relatively simple means of allowing teachers, students, museum curators and other educational personnel to build first-person explore-able virtual environments to quickly prototype without the need for extensive technical skills/knowledge or ridiculously large budgets.</p>
			<p>
				The steps of this process is as follows:
				
				<ol>
					<li>Build the environment using a&nbsp;free modeling software called <a href="http://www.sketchup.com/">SketchUp</a>.</li>
					<li>Convert model into an virtual environment with a&nbsp;virtual reality software called <a href="http://www.worldviz.com/vizard-virtual-reality-software/">Vizard</a>.</li>
					<li>Copy and paste a small snippet of code that transforms the environment into first-personal, explore-able space.</li>
					<li>Hook up the Oculus Rift and enjoy!</li>
				</ol>
			</p>
			<p>By utilizing a preset process, educators can focus more on developing quality material for their students, less so on the technical side and still creating cool and engaging content.</p>
			
